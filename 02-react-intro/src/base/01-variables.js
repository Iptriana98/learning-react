// Variables in JavaScript
// ========================

// A variable is a named container for storing data values.
// Use the `let` or `const` keyword to declare a variable.

// Declaring a variable using `let`
let name = "John Doe";
console.log(name); // Output: John Doe

// Declaring a variable using `const` (constant, cannot be reassigned)
const age = 30;
console.log(age); // Output: 30

// Variable reassignment
name = "Jane Doe";
console.log(name); // Output: Jane Doe

// Invalid reassignment of `const` variable will cause an error
// age = 31; // Uncaught TypeError: Assignment to constant variable.

